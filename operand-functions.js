export const addTwoNumbers = (_num1, _num2) => {
    return _num1 += _num2;
};

export const subtractTwoNumbers = (_num1, _num2) => {
    return _num1 -= _num2;
};

export const divideTwoNumbers = (_num1, _num2) => {
    return _num1 /= _num2;
};

export const multipleTwoNumbers = (_num1, _num2) => {
    return _num1 *= _num2;
};
